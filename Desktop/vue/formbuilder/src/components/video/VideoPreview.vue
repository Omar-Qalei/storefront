<template>
  <div class="widget">
    <SettingsWidget
      :show="item.i === getSelectedWidgetById.i"
      :item="item"
      :sectionId="sectionId"
    ></SettingsWidget>
    <iframe
      :src="getDefaultUrl(item) + item.properties.autoPlay"
      modestbranding="0"
      controls="0"
      frameborder="0"
      allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture"
      allowfullscreen
    ></iframe>
  </div>
</template>

<script>
import SettingsWidget from "../settings/SettingsWidget";
import { mapGetters } from "vuex";
export default {
  name: "VideoWidget",
  components: {
    SettingsWidget,
  },
  data() {
    return {
      items: [
        { title: "Click Me" },
        { title: "Click Me" },
        { title: "Click Me" },
        { title: "Click Me 2" },
      ],
    };
  },
  props: {
    item: {},
    sectionId: Number,
  },
  computed: {
    ...mapGetters(["getSelectedWidgetById"]),
  },
  methods: {
    getDefaultUrl: function(item) {
      // return item.properties.url + "?autoplay="
      if (item.properties.url) {
        const videoId = this.getId(item.properties.url);
        return this.convertToEmbedded(videoId);
      }
      return "https://www.youtube.com/embed/tbnzAVRZ9Xc?autoplay=";
    },
    convertToEmbedded: function(videoId) {
      const iframeMarkup =
        "https://www.youtube.com/embed/" + videoId + "?autoplay=";
      return iframeMarkup;
    },
    getId: function(url) {
      const regExp = /^.*(youtu.be\/|v\/|u\/\w\/|embed\/|watch\?v=|&v=)([^#&?]*).*/;
      const match = url.match(regExp);
      return match && match[2].length === 11 ? match[2] : null;
    },
  },
  mounted() {},
};
</script>

