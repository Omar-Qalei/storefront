<template>
  <div class="widget">
    <SettingsWidget
      :show="item.i === getSelectedWidgetById.i"
      :item="item"
      :sectionId="sectionId"
    ></SettingsWidget>
    <nav :style="menuStyle()">
      <a class="logo mr-6">
        Wimmly
      </a>
      <template v-if="getScreenSize.screen === 'web'">
        <div class="w-100" :style="{ textAlign: textAlign }">
          <v-btn
            v-for="(element, index) in list"
            :key="index"
            :style="[textStyle(), onHover(item.properties.elementHover)]"
            text
            @mouseover="hover = true"
            @mouseleave="hover = false"
            v-show="element.status"
          >
            {{ element.name }}
          </v-btn>
        </div>
      </template>
      <template v-else>
        <v-spacer></v-spacer>
        <v-btn
          :style="[
            { color: textStyle().color },
            onHover(item.properties.elementHover),
          ]"
          icon
          small
        >
          <v-icon>mdi-menu</v-icon>
        </v-btn>
      </template>
    </nav>
    <!-- <v-btn
      v-for="(item, index) in list"
      :key="index"
      text
      :style="[textStyle(), onHover(item.properties.elementHover)]"
    >
      {{ item.name }}
    </v-btn> -->
  </div>
</template>

<script>
import SettingsWidget from "../settings/SettingsWidget";
import { mapGetters } from "vuex";
export default {
  name: "MenuWidget",
  components: {
    SettingsWidget,
  },
  data() {
    return {
      drawer: false,
      list: [],
      textAlign: "center",
      text: {
        alignSelf: "center",
        fontWeight: "",
        fontStyle: "",
        textDecoration: "",
        color: "#FFFFFFFF",
        textTransform: "none",
        fontSize: "18px",
      },
      menu: {
        background: "transparent",
        fixedPosition: false,
      },
      hover: false,
    };
  },
  props: {
    item: {},
    sectionId: Number,
  },
  computed: {
    ...mapGetters(["getSelectedWidgetById", "getPages", "getScreenSize"]),
  },
  methods: {
    navStyle: function() {
      if (this.item.properties.style) {
        if (this.item.properties.style.menu) {
          return this.item.properties.style.menu
            ? this.item.properties.style.menu
            : this.menu;
        } else {
          return this.menu;
        }
      } else {
        return this.menu;
      }
    },
    menuStyle: function() {
      if (this.item.properties.style) {
        return this.item.properties.style.menu
          ? this.item.properties.style.menu
          : this.menu;
      } else {
        return this.menu;
      }
    },
    textStyle: function() {
      if (this.item.properties.style) {
        if (this.item.properties.style.elements) {
          this.textAlign = this.item.properties.style.elements.text.textAlign;
          return this.item.properties.style.elements.text
            ? this.item.properties.style.elements.text
            : this.text;
        } else {
          return this.text;
        }
      } else {
        return this.text;
      }
    },
    onHover: function(elementHover) {
      if (this.hover) {
        if (elementHover !== undefined) {
          return elementHover;
        }
      } else {
        return "";
      }
    },
  },
  mounted() {
    if (this.item.properties.fields.length > 0)
      this.list = this.item.properties.fields;
  },
};
</script>

